def 是否已知名称(名称: str) -> bool:
    """检查名称是否在 PreludeScope 中已知。"""
    return 名称 in 全部前置名称


def 是否内置函数(名称: str) -> bool:
    """检查名称是否为内置函数。"""
    return 名称 in 内置函数


def 是否模块名(名称: str) -> bool:
    """检查名称是否为已知模块名。"""
    return 名称 in 模块名称


def 查询模块成员(模块: str) -> frozenset[str]:
    """查询已知模块的公开成员名集合。"""
    return 模块成员映射.get(模块, frozenset())
