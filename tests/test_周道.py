def 转译并运行(源码: str) -> dict:
    """转译并执行周道源码，返回全局变量字典。"""
    return 运行(源码)


def 检查转译(源码: str, 期望_python: str):
    """检查转译结果是否与期望的 Python 一致。

    使用 转译_仅语法 跳过语义分析，以测试语法转换本身。
    """
    python_code = 转译_仅语法(源码)
    # 标准化：去除首尾空白，合并连续空白
    py_norm = " ".join(python_code.strip().split())
    exp_norm = " ".join(期望_python.strip().split())
    assert py_norm == exp_norm, f"\n期望: {期望_python}\n实际: {python_code}"


def 检查输出(源码: str, 期望输出: list[str]):
    """检查执行输出是否匹配。"""
    import io
    from contextlib import redirect_stdout

    f = io.StringIO()
    env = {"__name__": "__周道__"}
    python_code = 转译(源码)
    with redirect_stdout(f):
        exec(python_code, env)
    实际输出 = f.getvalue().strip().split("\n")
    assert 实际输出 == 期望输出, f"\n期望: {期望输出}\n实际: {实际输出}"


