class Test固定序列:
    def test_空固定序列(self):
        """空固定序列建立。"""
        r = 转译_仅语法("设a为固定序列［］。")
        assert "固定序列" in r or "序列" in r

    def test_单项(self):
        """单项固定序列。"""
        r = 转译_仅语法("设a为固定序列［1］。")
        assert "固定序列" in r or "序列" in r

    def test_多项(self):
        """多项固定序列。"""
        r = 转译_仅语法("设a为固定序列［1、2、3］。")
        assert "固定序列" in r or "序列" in r

    def test_索引(self):
        """固定序列索引访问。"""
        out = 运行并捕获("设a为固定序列［10、20、30］。运行如下：显示a［0］。")
        assert out == "10"

    def test_切片(self):
        """固定序列切片访问。"""
        运行并捕获("设a为固定序列［1、2、3、4］。运行如下：显示a［1：3］。")

