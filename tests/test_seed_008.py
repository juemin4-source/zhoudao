from contextlib import redirect_stdout


def 运行并捕获(源码: str) -> str:
    """运行周道程序并捕获 stdout。"""
    f = io.StringIO()
    env = {"__name__": "__周道__"}
    py = 转译(源码)
    with redirect_stdout(f):
        exec(py, env)
    return f.getvalue().strip()


